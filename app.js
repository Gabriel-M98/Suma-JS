// Desafío 1: Hacer una suma de dos numeros mediante un prompt
let numeroUsuario1 = parseFloat(prompt("Introduce un número"));
console.log(numeroUsuario1);

let numeroUsuario2 = parseFloat(prompt("Introduce un segundo número"));
console.log(numeroUsuario2);

let resultado = numeroUsuario1 + numeroUsuario2;
alert("El resultado de la suma es: " + resultado);
console.log(resultado);